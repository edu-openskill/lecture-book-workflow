#!/bin/bash
# 사용자 메시지에 "분석" 단어가 포함되면 발동
# Claude가 단순 grep·패턴 매칭이 아니라 모든 챕터 전체를 Read한 뒤 문맥 분석으로 판단하도록 강제

cat <<'EOF'
[분석 요청 감지]

다음 절차를 반드시 따를 것:

1. grep·패턴 매칭만으로 판단 금지. grep은 후보 좁히기에만 보조 사용
2. 작업 대상 책의 **모든 챕터 파일(01~06)** 을 Read해서 책 전반의 흐름·비유 매핑·용어 일관성을 파악
3. 각 후보 문장의 주어·역할·앞뒤 단락·챕터 호 위치를 직접 읽고 의미·맥락으로 판단
4. 분석 결과(이 문장이 왜 문제인지 / 왜 자연스러운지)를 사용자에게 함께 제시
5. 판단이 모호하면 자체 결정 금지, 사용자 결정 우선

이 절차를 건너뛰면 책 전체 일관성이 깨지고 같은 실수가 반복된다.
EOF
