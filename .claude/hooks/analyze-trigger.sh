#!/bin/bash
# 사용자 메시지에 "분석" 단어가 포함되면 발동
# Claude가 단순 grep·패턴 매칭이 아니라 모든 챕터 전체를 Read한 뒤
# 5개 렌즈를 무조건 검수하고, 해당사항 없을 때까지 반복하도록 강제

cat <<'EOF'
[분석 요청 감지 — 검수 + 반복 + 제안까지만]

"분석"은 분석하고 제안하라는 뜻이다. 파일을 고치라는 뜻이 아니다.
아래 절차를 반드시 따를 것. 하나라도 건너뛰면 분석은 무효다.

== 0. 파일 수정 금지 ==
0-1. 이 턴에서 Edit·Write·빌드 금지. 챕터/코드 파일을 건드리지 않는다
0-2. 산출물은 "분석 + 수정 제안" 텍스트뿐이다
0-3. 사용자가 제안을 보고 명시적으로 "적용/반영/ㄱㄱ/이걸로" 등 승인했을 때만,
     그때 별도 턴에서 Edit + 빌드한다. 승인 없이는 절대 손대지 않는다

== 1. 읽기 (판단 전제) ==
1-1. grep·패턴 매칭만으로 판단 금지. grep은 후보 자리 좁히기 보조로만 사용
1-2. 작업 대상 책의 **모든 챕터 파일(01~06)** 을 Read해서 흐름·비유 매핑·용어 일관성 파악
1-3. 각 후보 문장의 주어·역할·앞뒤 단락·챕터 호 위치를 직접 읽고 의미·맥락으로 판단

== 2. 5개 렌즈 무조건 검수 (전부, 매번) ==
요청에 어떤 단어가 있든 아래 5개를 전부 통과시킬 것. 일부만 보고 끝내지 말 것.

  ① 연결      — (1문장간) 앞 문장 결론 → 뒤 문장 전제가 자연스러운가.
                빠지거나 어색한 접속·조사(그래서/그런데/이/그/곧/도/는) 자리마다 판단.
                (2문단간 — 반드시 따로 본다) 각 단락 첫 어절을 확인한다.
                대비되는 두 단락을 둘 다 같은 비유로 cold start 하면 실패
                ("택배가 왔는지~" / "택배가 도착하면~" 나란히 던지기 금지).
                둘째 단락은 전환어(반대로·반면·그래서)로 시작하고
                앞 단락의 끝 키워드(헛걸음·멈춤·대기 등)를 명시적으로 회수하는가.
                "단락 첫 문장 ↔ 앞 단락 마지막 문장"을 한 쌍으로 점검한다.
                (3호) 설정→문제→해결→결과 호가 끊기지 않는가
  ② 코드 정합 — 본문 서술이 실제 예제 코드(code/ ex01~ex05)의 동작과 일치하는가.
                없는 실패·에러·응답·동작·시나리오를 지어내지 않았는가.
                코드블록·함수명·시그니처가 rag-end와 일치하는가. 의심되면 해당 ex 소스 Read
  ③ 라벨/AI투 — 라벨성 H2/대구, "~것입니다/~한 셈", AI투·억지·템플릿 문장,
                강조 반복("이것이 바로"), 절·챕터 번호 직접 참조가 없는가
  ④ 과거형    — 분위기 보강용·불필요한 과거형 회수("자리를 잡았다")가 없는가.
                서술 본문이 ~습니다/~입니다로 매듭지어지는가
  ⑤ 의인화    — 코드·시스템·객체에 사람 행위를 입히지 않았는가
                (비유 캐릭터 대사는 예외)

== 3. 반복 (해당사항 0이 될 때까지) — 머릿속/응답 안에서만 ==
3-1. 수정안 텍스트를 만든다 (파일에 쓰지 않는다)
3-2. 그 수정안을 **다시 읽고** 2번 5개 렌즈로 재분석한다 (수정이 새 문제를 만들었는지 포함)
3-3. 5개 렌즈 중 하나라도 걸리면 3-1로 돌아간다
3-4. 5개 렌즈 전부 깨끗할 때까지 반복. 깨끗해진 뒤에만 사용자에게 제안한다
3-5. 제안 시 각 자리의 "원본 → 수정안 → 어느 렌즈를 왜 통과했는지"를 함께 제시
3-6. 제안으로 끝낸다. 명확해 보여도 자동 적용 금지. 모든 자리는 사용자 결정 대상

== 4. 응답 형식 (챕터 본문 인용 분석에만 적용) ==
4-0. 적용 조건: 사용자가 챕터/프롤로그/에필로그 등 **본문 일부 문장을 직접 인용**하며
     분석을 요청한 경우에만 4-1~4-3을 따른다. hook·설정·코드·git·진행 상태 등
     본문 외 분석에는 4번을 적용하지 않고, 0~3번(파일 수정 금지·읽기·5렌즈·반복)만 따른다.
4-1. 인용 문장 주변만 보지 않는다. 반드시 인용 문장이 속한 **H3(절) 전체를 Read** 한다.
     인용 주변 10~30줄만 보고 단어 교정하면 같은 지적이 반복된다.
4-2. 분석 응답의 **첫 출력은 "## H3 전체 흐름" 표**다.
     | # | 단락 | 역할 |
     절 안의 모든 단락을 순서대로 나열한다 (도입/본문/그림/캡션/매핑 등).
4-3. 두 번째 출력은 **"## 수정 영향"** 섹션이다.
     - 앞 단락이 무엇으로 끝나는지 + 본 단락이 그걸 어떻게 받는지
     - 뒤 단락이 무엇을 전제로 시작하는지 + 본 단락이 그걸 어떻게 깔아주는지
     - 수정·삭제 시 끊기는 지점
4-4. 챕터 본문 인용임에도 두 섹션 누락 시 사용자는 "흐름부터 다시"로 재시도 트리거.

이 절차를 건너뛰거나 멋대로 파일을 고치면 신뢰가 깨지고 같은 실수가 반복된다.
EOF
